nome = "Victor Barbosa dos Santos"

print(nome[0]) # Primeira letra
print(nome[-1]) # Última letra
print(nome[:6]) # Tudo antes da 6 letra
print(nome[7:]) # Tudo da 7 letra em diante

print(nome[7:14]) # Tudo entre a 7 e a 14 letra
print(nome[7:14:2]) # Tudo entre a 7 e a 14 letra de 2 em 2

print(nome[:]) # Repetir nome
print(nome[::-1]) # Nome ao contrário
